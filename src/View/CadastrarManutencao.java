    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        String camposOK = "nao";

        if (jTextField1.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "A descrição deve ser Informado!");
        } else if (jTextField3.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "A Data deve Ser Informada!");
        } else if (jTextField2.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "O número da OS deve ser Informado!");

        } else {
            camposOK = "sim";
        }

        if (camposOK == "sim") {

            String TipoManutencao = String.valueOf(jComboBox1.getSelectedItem());
            String placa = String.valueOf(jComboBox2.getSelectedItem());
            String DescricaoManutencao = jTextField3.getText();
            String StatusManutencao = String.valueOf(jComboBox3.getSelectedItem());
            String DataManutencao = jTextField2.getText();
            String numeroOS = jTextField3.getText();

            facManutencao.cadastrarManutencao(TipoManutencao, placa, DescricaoManutencao, StatusManutencao, DataManutencao, numeroOS);

            jTextField1.setText("");
            jTextField3.setText("");
            jTextField2.setText("");

        }
    }//GEN-LAST:event_jButton2ActionPerformed


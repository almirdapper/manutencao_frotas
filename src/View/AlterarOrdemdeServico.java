    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       String camposOK = "nao";
        
        if(jTextField1.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null, "O id da OS ser Informado!");
        }else {
            camposOK = "sim";
             jTextField1.setEditable(false);
         jTextField1.setBackground(Color.yellow);
        }
        
        if(camposOK == "sim"){
        int id = Integer.parseInt(jTextField1.getText());
       
       dadosOS = facOS.listarOSid(id);
       
       for(OrdemDeServico o: dadosOS){
           jFormattedTextField1.setText(o.getData());
           jComboBox1.setSelectedItem(o.getPlacaVeiculo());
           jComboBox2.setSelectedItem(o.getStatus());
           jComboBox3.setSelectedItem(o.getItemOS());
           jComboBox4.setSelectedItem(o.getServico());
           jTextField2.setText(o.getObservacao());
       }
       
        
        
         
        
        }
    }//GEN-LAST:event_jButton1ActionPerformed


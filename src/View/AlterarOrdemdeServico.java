        String placadoVeiculo = (String) jComboBox1.getSelectedItem();
        String status = (String) jComboBox2.getSelectedItem();
        String item = (String) jComboBox3.getSelectedItem();
        String servico = (String) jComboBox4.getSelectedItem();
